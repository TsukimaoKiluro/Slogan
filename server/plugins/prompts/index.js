/**
 * 获取提示词内容
 * @param {string} promptId - 提示词ID
 * @param {string} customPrompt - 自定义提示词内容（用户提示词）
 * @returns {string} 提示词内容
 */
export function getPromptContent(promptId, customPrompt = null) {
  // 如果有自定义提示词，直接使用
  if (customPrompt) {
    return customPrompt
  }
  
  // 从预设中查找
  if (promptId && presetPrompts[promptId]) {
    return presetPrompts[promptId]
  }
  
  // 默认使用莲子
  return presetPrompts.preset_renko
}

